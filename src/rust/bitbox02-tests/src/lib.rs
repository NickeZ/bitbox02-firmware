
#[cfg(test)]
mod tests {

    #[test]
    fn salt_hash_data() {
        let data: &[u8] = &[0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88];
        let hash = bitbox02::salt::hash_data(data, "test purpose");
        assert!(hash.is_ok());
        let expected_result: [u8; 32] = [
            0x62, 0xdb, 0x8d, 0xcd, 0x47, 0xdd, 0xf8, 0xe8, 0x18, 0x09, 0xc3,
            0x77, 0xed, 0x96, 0x64, 0x38, 0x55, 0xd3, 0x05, 0x2b, 0xb7, 0x32,
            0x37, 0x10, 0x0c, 0xa8, 0x1f, 0x0f, 0x5a, 0x76, 0x11, 0xe6,
        ];
        assert_eq!(hash.unwrap(), expected_result);
    }
}
